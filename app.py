import streamlit as st

# --- 1. アプリの基本設定 ---
st.set_page_config(page_title="こどもサポートAI", page_icon="🧸", layout="centered")

st.title("🧸 こどもサポートAI (プロトタイプ)")
st.write("発達特性のあるお子様の状態に合わせて、専門知識（論文・ガイドライン等）から具体的な対応の選択肢を提案します。")

# --- 2. ユーザーからの情報入力フォーム ---
st.header("1. お子様の状態を入力")
with st.form("input_form"):
    age = st.selectbox("お子様の年齢", ["未就学児", "小学生(低学年)", "小学生(高学年)", "中高生"])
    
    # 複数選択可能な特性リスト
    characteristics = st.multiselect(
        "特性や気になること（複数選択可）", 
        ["視覚優位", "聴覚過敏", "言葉の遅れ", "こだわりが強い", "多動・衝動性", "見通しへの不安", "感覚探求"]
    )
    
    situation = st.text_area(
        "現在の困りごとや、具体的な状況を教えてください", 
        placeholder="例：スーパーに行くと、店内放送などの大きな音を嫌がってパニックになってしまう。"
    )
    
    # 送信ボタン
    submitted = st.form_submit_button("この状況にあった対応策を検索する")

# --- 3. AIからの提案（RAG機能のイメージ） ---
if submitted:
    if not situation:
        st.warning("具体的な状況を入力してください。")
    else:
        st.success("📚 登録された専門データベースから関連する事例と対応策を検索しました。")
        
        # ※本来はここでOpenAIやGeminiのAPIを呼び出し、RAGを使って回答を生成します。
        # 今回はイメージをつかむためのダミーテキストを表示します。
        
        st.markdown("### 💡 AIからの提案（アクションの選択肢）")
        st.write("入力された「聴覚過敏」「パニック」などの情報に基づき、以下の対応をご提案します。お子様に合いそうなものを試してみてください。")
        
        st.info("""
        **選択肢A：事前の環境調整（予防）**
        * スーパーに行く前に、ノイズキャンセリングイヤホンやイヤーマフを装着させる。
        * 混雑する時間帯（夕方など）を避け、比較的静かな午前中に買い物に行く。
        
        **選択肢B：見通しの提示（安心感の付与）**
        * 「スーパーにいる時間は10分だけ」「買うものはこれとこれ」と、イラストや写真カードを使って事前にスケジュールを伝えておく。
        
        **選択肢C：事後対応（パニック発生時）**
        * パニックが起きたら無理になだめず、すぐに静かな車内や店の外など、刺激の少ない場所に移動して落ち着くまで見守る。
        """)
        
        # フィードバック画面を表示するためのスイッチをオンにする
        st.session_state['show_feedback'] = True

# --- 4. 実行結果のフィードバック（学習ループの要） ---
if st.session_state.get('show_feedback'):
    st.divider()
    st.header("2. 試してみた結果を教えてください")
    st.write("実践した結果を入力することで、AIが学習し、次回以降（他の人にも）より効果的な提案ができるようになります。")
    
    with st.form("feedback_form"):
        selected_action = st.radio(
            "どのアクションを試しましたか？", 
            ["選択肢A（事前の環境調整）", "選択肢B（見通しの提示）", "選択肢C（事後対応）", "その他（独自の方法）"]
        )
        
        result_rating = st.slider("効果はありましたか？（1: 悪化した ～ 5: とても効果があった）", 1, 5, 3)
        
        feedback_memo = st.text_area("気づいたことや、うまくいった/いかなかった詳細をメモしてください", placeholder="例：イヤーマフは嫌がって外してしまったが、時間を10分と決めたら我慢できた。")
        
        feedback_submitted = st.form_submit_button("フィードバックを送信してAIに学習させる")
        
        if feedback_submitted:
            st.success("🎉 フィードバックを保存しました！")
            st.write("いただいたデータは知識データベースに蓄積され、今後の提案精度向上に役立てられます。ご協力ありがとうございます！")
            # ※本来はここで入力された評価とメモをデータベース（Supabaseなど）に保存します。
